package com.optimasc.text;

/** Contains constants for SimpleDateFormat parsing of dates. */
public class DatePatterns
{
  /** Date/time pattern from IETF RFC 822 / IETF RFC 2822 standard */
  public static final String DATE_IETF_RFC_822 = "EEE, dd MMM yyyy HH:mm:ss Z";
  /** Date/time pattern from ISO/IEC 8824 and PDF properties */
  public static final String DATE_ISO_8824 = "yyyyMMddHHmmssZ";
  /** Date/time pattern from EXIF specification */
  public static final String DATE_EXIF = "yyyy:MM:dd HH:mm:ss";
}
